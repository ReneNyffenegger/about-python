numbers = ['zero', 'one', 'two', 'three', 'four', 'five', 'six']

print(numbers[4])
#
# four

print(numbers[2:5])
#
# ['two', 'three', 'four']

print(numbers[-2])
#
# five

print(numbers[-3:-1])
#
# ['four', 'five']

print(numbers[-3:])
#
# ['four', 'five', 'six']

print(numbers[5:])
#
# ['five', 'six']

print(numbers[:])
#
# ['zero', 'one', 'two', 'three', 'four', 'five', 'six']
