lst = [ 'zero', 'one', 'two', 'three', 'four', 'five', 'six' ]

print( lst[4:] )
#
# ['four', 'five', 'six']

print( lst[:4] )
#
# ['zero', 'one', 'two', 'three']

print( lst[1:6] )
#
# ['one', 'two', 'three', 'four', 'five']

print( lst[1:-1] )
#
# ['one', 'two', 'three', 'four', 'five']

print( lst[1:-1:2] )
#
# ['one', 'three', 'five']

print( lst[:] )
#
# ['zero', 'one', 'two', 'three', 'four', 'five', 'six']
